function createDatapoints(val: number) {
  const result: protos.cockroach.ts.tspb.TimeSeriesDatapoint[] = [];
  for (let i = 0; i < val; i++) {
    result.push(
      new protos.cockroach.ts.tspb.TimeSeriesDatapoint({
        timestamp_nanos: new Long(val),
        value: val,
      }),
    );
  }
  return result;
}

